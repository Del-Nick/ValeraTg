def draw_rectangle(floor: int | tuple, room: str) -> Image:
    alpha = floors[floor]
    im = white_layer.copy()

    if type(floors_coord[floor][room][0]) is list:
        for rect in floors_coord[floor][room]:
            x_1, y_1, x_2, y_2 = rect
            ImageDraw.Draw(im).rectangle((x_1, y_1, x_2, y_2), fill=(255, 145, 0))
    else:
        x_1, y_1, x_2, y_2 = floors_coord[floor][room]
        ImageDraw.Draw(im).rectangle((x_1, y_1, x_2, y_2), fill=(255, 145, 0))

    if room == 'ниияф':
        ImageDraw.Draw(im).rectangle((1985, 372, 2089, 777), fill=(255, 145, 0))
